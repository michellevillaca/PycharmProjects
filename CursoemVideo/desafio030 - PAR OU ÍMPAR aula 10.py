num=int(input('Digite um número inteiro: '))
#Um número é par se for divisível por 2, ou seja, se o resto da divisão por 2 for zero. Um número é ímpar se não for divisível por 2, ou seja, se o resto da divisão por 2 for 1
if num%2 == 0:
    print('Esse número é par!')
else:
    print('Esse número é ímpar!')